import mongoose from "mongoose";

export const userschema = new mongoose.schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    role: {
      type: Number,
      required: true,
    },
  },
  {
    timeStamp: true,
  }
);
